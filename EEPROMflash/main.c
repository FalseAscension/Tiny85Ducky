#include <avr/eeprom.h>
#include "Light_WS2812/light_ws2812.h"

#define PROGSIZE 68
uint8_t PROG[PROGSIZE] = {  0xe9, 0xe8, 0x03, 0x00, 0x00, 0xed, 0x08, 0x15, 0xee, 0x08, 0xe9, 0x64, 0x00, 0x00, 0x00, 0x0b, 
                            0x17, 0x17, 0x13, 0x16, 0xef, 0x33, 0x38, 0x38, 0x1a, 0x1a, 0x1a, 0x37, 0x1c, 0x12, 0x18, 0x17, 
                            0x18, 0x05, 0x08, 0x37, 0x06, 0x12, 0x10, 0x38, 0x1a, 0x04, 0x17, 0x06, 0x0b, 0xef, 0x38, 0x19, 
                            0x2e, 0x07, 0xef, 0x14, 0x1a, 0x21, 0x1a, 0x26, 0xef, 0x1a, 0x0a, 0xef, 0x1b, 0x06, 0xef, 0x14, 
                            0x28, 0xe8, 0x05, 0x00};

struct cRGB led[1];

int main() {
    
    led[0].r=120;
    led[0].g=0;
    led[0].b=0;
    ws2812_setleds(led, 1);

    for(int i = 0; i < PROGSIZE; i++)
        eeprom_write_byte((uint8_t*) i, PROG[i]);
    
    led[0].r=0;
    led[0].g=120;
    led[0].b=0;
    ws2812_setleds(led, 1);


    while(1);

    return 0;
}
